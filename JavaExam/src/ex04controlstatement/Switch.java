package ex04controlstatement;

import java.util.Scanner;
/*
switch문 : if문 처럼 조건에 따라 분기하는 제어문
	형식
		swtch(정수식 or 산술식){
			case 값1:
				실행문1; break;
			case 값2:
				실행문2; break;
			default:
				위 모든 조건에 위배될때 실행되는 문장
				else와 같은 의미라 보면된다.
		}
		switch문 내부에서 break문을 만나게디면 실행이
		중지되고 밖으로 빠져나오게된다.
 */
public class Switch {

	public static void main(String[] args) {
		
		/*
		Scanner클래스: JDK5.0부터 추가된 클래스로 키보드를
			통해 사용자로부터 값을 입력받을때 사용한다.
			nextLine() : 문자열을 입력받음
			nextInt() : 정수를 입력받음
		 */
		Scanner scanner =new Scanner(System.in);
		System.out.print("숫자를 입력하세요");
		int iNum = scanner.nextInt();
		System.out.println("입력한 숫자는:"+ iNum);
		
		int remain = iNum % 3;
		
		/*
		만약 switch문에 break문을 사용하지 않으면 그 아래에 있는
		문장들이 실행되므로 주의해야 한다.
		즉, 입력값이 9인경우 세개의 문장 모두가 실행되게 된다.
		 */
		switch (remain) {
			case 0:
				System.out.println("나머지는 0입니다.");
				break;
			case 1:
				System.out.println("나머지는 1입니다.");
				break;
			default:
				System.out.println("나머지는 2입니다.");
		}
		/*
		switch문 사용시 주의할점1
		-long타입의 변수는 사용이 불가하다.
		-byte/short/int/char/String형만 사용 가능함.
		 */
		
		/*long ln =100;
		
		switch(ln) {
		case 100:
			System.out.println("long타입 사용??");
		default:
			System.out.println("안되는군!");
		}*/
		/*
		switch문 사용시 주의할점2
		-비교식이나 논리식은 사용불가함.
		 */
		
		/*
		switch(iNum%3==0) {
			System.out.print("논리식도 안돠는군~");
		}*/
		
		String title = "JAVA";
		switch(title) {
		case "자바": System.out.println("자바 좋아");
			break;
		case "JAVA": System.out.println("JAVA Gooood");
		}
		/*
		여러 case를 동시에 처리할때는 break문없이 case를 나열한다.
		if문에서 ||(논리OR)로 조건을 연결한것과 동일하다.
		 */
		int season = 4;
		switch(season) {
		case 4: case 5: case 6:
			System.out.println("봄입니다.");
			break;
		case 7: case 8: case 9:
			System.out.println("여름입니다.");
			break;
		case 10:
			System.out.println("가을입니다.");
			break;
		case 11: case 12: case 1: case 2:
			System.out.println("겨울입니다.");
			break;
		}

	}

}
